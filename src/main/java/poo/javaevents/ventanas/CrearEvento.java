    private void crearEventoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_crearEventoActionPerformed
        // TODO add your handling code here:
        try {
            if (campoTitulo.getText().trim().isEmpty()) {
                throw new ExcepcionCampoVacio("título");
            }
            if (campoDireccion.getText().trim().isEmpty()) {
                throw new ExcepcionCampoVacio("dirección");
            }
            if (campoCiudad.getText().trim().isEmpty()) {
                throw new ExcepcionCampoVacio("ciudad");
            }
            String calle = campoDireccion.getText();
            String ciudad = campoCiudad.getText();
            String titulo = campoTitulo.getText();
            Object tipoSelec = campoTipo.getSelectedItem();  
            String tipo = tipoSelec.toString();

            campoNumero.commitEdit();
            Number numero = (Number) campoNumero.getValue();
            int num = numero.intValue();
            if (numero == null) {
                throw new ExcepcionCampoVacio("numero");
            }

            campoCodPostal.commitEdit();
            Number codPostal = (Number) campoCodPostal.getValue();
            int CP = codPostal.intValue();
            if (codPostal == null) {
                throw new ExcepcionCampoVacio("día");
            }
            
            campoPrecio.commitEdit();
            Number precio = (Number) campoPrecio.getValue();
            double precioEntrada = precio.doubleValue();
           
           
            Direccion d = new Direccion(calle, num, ciudad, CP);
            Evento e = new Evento(titulo, tipo, d, fechasEvento, precioEntrada, rutaImagenPortada, 0);
            if(Datos.eventos.contains(e)){
                eventoExistente.showMessageDialog(null, "El evento ya existe.");
            }
            else{
            Datos.eventos.add(e);
            Datos.guardarEventos();
            }
        } catch (ExcepcionCampoVacio ex) {
            campoVacio.showMessageDialog(null, "Error: " + ex.getMessage());
        } catch (ParseException pe) {
            errorFormato.showMessageDialog(null, "Hay un error en el formato");
        }
        


    }//GEN-LAST:event_crearEventoActionPerformed

