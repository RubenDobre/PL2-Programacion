    private void selecPortadaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_selecPortadaActionPerformed
        // TODO add your handling code here:
        JFileChooser fileChooser = new JFileChooser();

        // Filtrar para que solo muestre imágenes comunes
        fileChooser.setFileFilter(new javax.swing.filechooser.FileNameExtensionFilter(
                "Archivos de imagen", "jpg", "jpeg", "png", "bmp"));

        int resultado = fileChooser.showOpenDialog(null);
        if (resultado == JFileChooser.APPROVE_OPTION) {
            java.io.File archivo = fileChooser.getSelectedFile();

            // Mostrar la imagen en el label (redimensionada)
            ImageIcon icon = new ImageIcon(archivo.getAbsolutePath());
            Image img = icon.getImage().getScaledInstance(150, 150, Image.SCALE_SMOOTH);
            imagenPortada.setIcon(new ImageIcon(img));

            // Aquí puedes guardar la ruta o el archivo para luego usarlo
            // Por ejemplo:
            this.rutaImagenPortada = archivo.getAbsolutePath();
    }//GEN-LAST:event_selecPortadaActionPerformed
    }
    protected ArrayList<LocalDateTime> fechasEvento = new ArrayList<>();
    private void anadirFechaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_anadirFechaActionPerformed
        // TODO add your handling code here:
        try {
            campoDia.commitEdit();   // obliga a validar el campo
            campoHora.commitEdit();

            // Obtener los valores como Date
            Date fecha = (Date) campoDia.getValue();
            Date hora = (Date) campoHora.getValue();

            // Convertir a LocalDateTime
            Instant instantFecha = fecha.toInstant();
            Instant instantHora = hora.toInstant();

            // Pasar a ZonedDateTime para obtener la zona horaria local
            ZoneId zona = ZoneId.systemDefault();
            LocalDate localFecha = instantFecha.atZone(zona).toLocalDate();
            LocalTime localHora = instantHora.atZone(zona).toLocalTime();

            LocalDateTime fechaHora = LocalDateTime.of(localFecha, localHora);
            if (!fechasEvento.contains(fechaHora)) {
                fechasEvento.add(fechaHora);
                JOptionPane.showMessageDialog(null, "Fecha del evento añadida correctamente.");
            } else {
                JOptionPane.showMessageDialog(null, "Fecha ya añadida.");
            }

        } catch (ParseException | NullPointerException e) {
            JOptionPane.showMessageDialog(null, "Error en el formato de fecha u hora.");
        }
    }//GEN-LAST:event_anadirFechaActionPerformed

